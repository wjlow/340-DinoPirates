package Model;

/**
 * This class contains all the parameters that will be used by BLAST and the
 * number of cores.
 * There should only be one Parameters object at any one time during runtime.
 * @author wjlow and wengn
 */
public class Parameters
{
    private String text;
    private String flag;
    private String value;

    public Parameters(String text, String flag, String value)
    {
        this.text = text;
        this.flag = flag;
        this.value = value;
    }

    public String getText()
    {
        return text;
    }

    public String getFlag()
    {
        return flag;
    }

    public String getValue()
    {
        return value;
    }

    public void setText(String s)
    {
        text = s;
    }

    public void setFlag(String s)
    {
        flag = s;
    }

    public void setValue(String s)
    {
        value = s;
    }
}
